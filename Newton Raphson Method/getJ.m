function J=getJ(x)
%constants
k1=1;
k2=5;
k3=10;
q=1;
v=10;
J=zeros(4);
J(1,1)=-q-k1*x(2)*v;
J(1,2)=-k1*x(2)*v;
J(1,3)=k1*x(2)*v;
J(1,4)=0;
J(2,1)=-k1*x(1)*v;
J(2,2)=-q-k1*x(1)*v-k2*x(3)*v;
J(2,3)=k1*x(1)*v-k2*x(3)*v;
J(2,4)=k2*x(3)*v;
J(3,1)=0;
J(3,2)=-k2*x(2)*v;
J(3,3)=-q-k2*x(2)*v;
J(3,4)=k2*x(2)*v;
J(4,1)=0;
J(4,2)=k3*v;
J(4,3)=k3*v;
J(4,4)=-q-k3*v;
J=transpose(J);